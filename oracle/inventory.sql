CREATE TABLE orders (
	id numeric(9,0) GENERATED BY DEFAULT AS IDENTITY START WITH 10001 NOT NULL PRIMARY KEY,
	order_date DATE NOT NULL,
	purchaser numeric(9,0) NOT NULL,
	quantity numeric(9,0) NOT NULL,
	product_id numeric(9,0) NOT NULL,
	FOREIGN KEY (purchaser) REFERENCES customers(id),
	FOREIGN KEY (product_id) REFERENCES products(id)
